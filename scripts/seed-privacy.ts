function createBlockContent(sections: { style: string; text: string; marks?: string[] }[]) {
  return sections.map((section, index) => ({
    _type: 'block',
    _key: `block-${index}`,
    style: section.style,
    children: [
      {
        _type: 'span',
        _key: `span-${index}`,
        text: section.text,
        marks: section.marks || [],
      },
    ],
    markDefs: [],
  }))
}

